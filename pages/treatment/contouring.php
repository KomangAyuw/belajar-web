            <div class="types-grid">
                <div class="type-card">
                    <h3>CoolSculpting</h3>
                    <p>Teknologi cryolipolysis non-invasif yang membekukan dan menghancurkan sel lemak secara permanen tanpa pembedahan atau downtime.</p>
                    <span class="type-recovery">Pemulihan: Tidak ada</span>
                </div>
                <div class="type-card">
                    <h3>Liposuction</h3>
                    <p>Prosedur bedah untuk menghilangkan lemak secara langsung dari area yang ditargetkan, memberikan hasil yang lebih dramatis dan presisi.</p>
                    <span class="type-recovery">Pemulihan: 1–2 minggu</span>
                </div>
                <div class="type-card">
                    <h3>HIFU Body</h3>
                    <p>Menggunakan gelombang ultrasonik terfokus untuk memecah sel lemak dan mengencangkan kulit dari dalam, tanpa sayatan.</p>
                    <span class="type-recovery">Pemulihan: 1–2 hari</span>
                </div>
                <div class="type-card">
                    <h3>RF Body Tightening</h3>
                    <p>Radiofrequency untuk mengencangkan kulit kendur dan meningkatkan produksi kolagen, ideal setelah penurunan berat badan.</p>
                    <span class="type-recovery">Pemulihan: Tidak ada</span>
                </div>
                <div class="type-card">
                    <h3>Tummy Tuck</h3>
                    <p>Abdominoplasty untuk menghilangkan kulit dan lemak berlebih di perut, serta mengencangkan otot dinding perut.</p>
                    <span class="type-recovery">Pemulihan: 3–4 minggu</span>
                </div>
                <div class="type-card">
                    <h3>EMSculpt</h3>
                    <p>Teknologi elektromagnetik untuk membangun otot dan membakar lemak sekaligus, setara dengan ribuan sit-up dalam satu sesi.</p>
                    <span class="type-recovery">Pemulihan: Tidak ada</span>
                </div>
            </div>
